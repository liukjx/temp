def calculator(num1,num2):
    """
    这是一个计算两个参数做四则运算的函数，
    该函数的作用是用户传入两个参数，对传输的两个参数进行计算
    :param num1: 传入一个数值型数据
    :param num2: 传入另一个数值型数据
    :return: 根据传入的数值型数据做四则运算，使用print函数进行打印，故此函数无返回值
    """
    print(num1+num2)
    print(num1*num2)
    print(num1-num2)
    print(num1/num2)

calculator()


