#定义字符串
num="0123456789"
print(num[2:5:1])
#步长 :正 负
#开始：2
#结束：5
#原则：只顾头来尾不顾   2 3 4
print(num[2:5])  #如果不指定步长默认为1
print(num[:5]) # 0 1 2 3 4  #如果没有指定开始，默认从0开始
print(num[1:]) #如果没有结尾，代表从开始点截取整个字符串
print(num[:]) #相当于整个字符串复制一遍  num[0:]
print(num[::2])#步长为2，打印偶数  num[0::2]

print(num[::-1]) #num[0::-1]
print(num[-4:-1:1]) #678